CREATE VIEW [dbo].[MarketTradesByDateView]
AS
SELECT        Market, CONVERT(date, DateUtc) AS Date, ROUND(AVG(Price), 8) AS AvgPrice, ROUND(MIN(Price), 8) AS MinPrice, ROUND(MAX(Price), 8) AS MaxPrice, ROUND(SUM(AmountQuote), 4) AS VolumeQuote, ROUND(SUM(AmountBase),
                          4) AS VolumeBase, ROUND(SUM(AmountBase) / SUM(AmountQuote), 8) AS AvgPriceByVolume, TradeType
FROM            dbo.MarketTradesView
GROUP BY Market, CONVERT(date, DateUtc), TradeType
GO

